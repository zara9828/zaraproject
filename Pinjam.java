public class Pinjam {
    private String no_transaksi;
    private String nik;
    private String kd;
    private String jumlah_pinjam;
    private String tgl_pinjam;
    private String harga_pinjam;
    
    public String getNo_Transaksi() {
        return no_transaksi;
    }
    
    public void setNo_Transaksi (String no_transaksi) {
        this.no_transaksi = no_transaksi;
    }
    
    public String getNik () {
        return nik;
    }
    
    public void setNik (String nik) {
        this.nik = nik;
    }
    
    public String getKd () {
        return kd;
    }
    
    public void setKd (String kd) {
        this.kd = kd;
    }
    
    public String getJumlah_Pinjam () {
        return jumlah_pinjam;
    }
    
    public void setJumlah_Pinjam (String jumlah_pinjam) {
        this.jumlah_pinjam = jumlah_pinjam;
    }
    
    public String getTgl_pinjam () {
        return tgl_pinjam;
    }
    
    public void setTgl_pinjam (String tgl_pinjam) {
        this.tgl_pinjam = tgl_pinjam;
    }
    
    public String getHarga_pinjam () {
        return harga_pinjam;
    }
    
    public void setHarga_pinjam (String harga_pinjam) {
        this.harga_pinjam = harga_pinjam;
    }
        
}
    